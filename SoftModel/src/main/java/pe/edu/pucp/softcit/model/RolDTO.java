public class RolDTO {
    private Integer idRol;
    private String nombreRol;

    public RolDTO() {
        this.idRol = null;
        this.nombreRol = null;
    }

    public Integer getIdRol() {
        return idRol;
    }

    public void setIdRol(Integer idRol) {
        this.idRol = idRol;
    }

    public String getNombreRol() {
        return nombreRol;
    }

    public void setNombreRol(String nombreRol) {
        this.nombreRol = nombreRol;
    }
    
}
